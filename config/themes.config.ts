/**
 * Theme Configuration — Color themes for AI Launch Space
 *
 * HOW TO ADD A NEW THEME FROM tweakcn.com:
 * 1. Go to https://tweakcn.com/editor/theme
 * 2. Pick any preset (or customize your own)
 * 3. Click "Code" button → select "Tailwind v3 (HSL)" format
 * 4. Copy the HSL values for :root (light) and .dark into a new entry below
 * 5. You only need to override the variables that differ from "default"
 *
 * The "default" theme values live in globals.css (:root / .dark blocks).
 * Each color theme here OVERRIDES those base values.
 *
 * Light/dark mode is handled by next-themes (class="dark" on <html>).
 * Color themes are handled via data-color-theme attribute on <html>.
 */

